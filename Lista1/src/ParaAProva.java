// Dicas para a prova de Java:

// 1. Tipos de Variáveis
// - int: Armazena números inteiros (ex: int idade = 25;)
// - double: Armazena números decimais (ex: double altura = 1.75;)
// - char: Armazena um único caractere (ex: char letra = 'A';)
// - String: Armazena uma cadeia de caracteres (ex: String nome = "João";)
// - boolean: Armazena verdadeiro ou falso (ex: boolean ativo = true;)

// 2. Operadores Aritméticos
// - +: Soma (ex: int soma = a + b;)
// - -: Subtração (ex: int diferença = a - b;)
// - *: Multiplicação (ex: int produto = a * b;)
// - /: Divisão (ex: double quociente = a / b;)
// - %: Módulo (resto da divisão) (ex: int resto = a % b;)

// 3. Estruturas Condicionais
// - if: Executa um bloco de código se a condição for verdadeira (ex: if (idade > 18) {...})
// - else if: Verifica uma nova condição se a anterior for falsa (ex: else if (idade == 18) {...})
// - else: Executa um bloco de código se todas as condições anteriores forem falsas (ex: else {...})

// 4. Laços de Repetição
// - for: Repetição com um contador (ex: for (int i = 0; i < 5; i++) {...})
// - while: Repetição enquanto a condição for verdadeira (ex: while (x < 10) {...})
// - do-while: Repetição que sempre executa pelo menos uma vez (ex: do {...} while (x < 10);)

// 5. Métodos
// - Definição: public static tipo nomeMetodo(tipo parametro) {...}
// - Exemplo: public static int soma(int a, int b) { return a + b; }
// - Chamada: int resultado = soma(5, 3);

// 6. Tipos de Contas Comuns
// - Soma: int soma = a + b;
// - Subtração: int diferença = a - b;
// - Multiplicação: int produto = a * b;
// - Divisão: double quociente = a / b;
// - Módulo: int resto = a % b;

// 7. Casting (Conversão de Tipos)
// - Converter de double para int: int inteiro = (int) valor;
// - Converter de int para double: double decimal = (double) inteiro;

// 8. Entrada e Saída
// - Leitura de dados: Scanner sc = new Scanner(System.in);
// - int x = sc.nextInt(); // lê um inteiro
// - double y = sc.nextDouble(); // lê um decimal
// - String s = sc.next(); // lê uma palavra
// - sc.nextLine(); // lê uma linha completa

// 9. Trabalhando com Arrays
// - Declaração e inicialização: int[] numeros = {1, 2, 3, 4, 5};
// - Acesso a elementos: int primeiro = numeros[0]; // primeiro = 1

// 10. Manipulação de Strings
// - Comparação: boolean iguais = s1.equalsIgnoreCase(s2); // true ou false
// - Concatenar: String saudacao = "Olá, " + "Mundo!";

// 11. Operadores Relacionais e Lógicos
// - Relacionais: ==, !=, >, <, >=, <=
// - Lógicos: && (e), || (ou), ! (não)

