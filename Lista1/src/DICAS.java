// Aqui estão algumas dicas em Java e tipos de contas que podem ser úteis para uma prova:

// 1. Tipos de Variáveis
//    - int: Inteiros (ex: 10, -3)
//    - double: Números decimais (ex: 3.14, -0.001)
//    - char: Caracteres únicos (ex: 'A', 'b')
//    - String: Cadeias de caracteres (ex: "Olá", "Java")
//    - boolean: Verdadeiro ou falso (true ou false)

// 2. Operadores Aritméticos
//    - +: Soma
//    - -: Subtração
//    - *: Multiplicação
//    - /: Divisão
//    - %: Módulo (resto da divisão)

// 3. Estruturas Condicionais
//    - if: Executa um bloco de código se uma condição for verdadeira.
//    - else if: Verifica outra condição se a anterior for falsa.
//    - else: Executa um bloco de código se todas as condições anteriores forem falsas.

//    Exemplo:
//    int num = 10;
//    if (num > 0) {
//        System.out.println("Positivo");
//    } else if (num < 0) {
//        System.out.println("Negativo");
//    } else {
//        System.out.println("Zero");
//    }

// 4. Laços de Repetição
//    - for: Repetição controlada por contador.
//    - while: Repetição que continua enquanto a condição for verdadeira.
//    - do-while: Repetição que executa pelo menos uma vez.

//    Exemplo de for:
//    for (int i = 0; i < 5; i++) {
//        System.out.println(i);
//    }

// 5. Métodos
//    - Definição de uma função/método:
//      public static int soma(int a, int b) {
//          return a + b;
//      }
//    - Chamando um método:
//      int resultado = soma(5, 3);
//      System.out.println(resultado);

// 6. Tipos de Contas Comuns
//    - Soma:
//      int a = 5, b = 10;
//      int soma = a + b;  // soma = 15

//    - Subtração:
//      int a = 10, b = 3;
//      int subtracao = a - b;  // subtracao = 7

//    - Multiplicação:
//      int a = 4, b = 5;
//      int multiplicacao = a * b;  // multiplicacao = 20

//    - Divisão:
//      double a = 20, b = 4;
//      double divisao = a / b;  // divisao = 5.0

//    - Módulo:
//      int a = 10, b = 3;
//      int modulo = a % b;  // modulo = 1

// 7. Casting (Conversão de Tipos)
//    - Para converter de double para int:
//      double valor = 9.8;
//      int inteiro = (int) valor;  // inteiro = 9

// 8. Entrada e Saída
//    - Leitura de dados:
//      Scanner sc = new Scanner(System.in);
//      int x = sc.nextInt();  // lê um inteiro
//      double y = sc.nextDouble();  // lê um decimal
//      String s = sc.next();  // lê uma palavra
//      sc.nextLine();  // lê uma linha completa

//    - Saída de dados:
//      System.out.println("Resultado: " + x);

// 9. Trabalhando com Arrays
//    - Declaração e inicialização de arrays:
//      int[] numeros = {1, 2, 3, 4, 5};

//    - Acessar elementos do array:
//      int primeiro = numeros[0];  // primeiro = 1

// 10. Manipulação de Strings
//    - Comparação de Strings:
//      String s1 = "Java";
//      String s2 = "java";
//      boolean iguais = s1.equalsIgnoreCase(s2);  // true

//    - Concatenar Strings:
//      String saudacao = "Olá, " + "Mundo!";

// 11. Operadores Relacionais e Lógicos
//    - Relacionais: ==, !=, >, <, >=, <=
//    - Lógicos: && (e), || (ou), ! (não)

